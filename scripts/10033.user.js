            formatCompactTime: function (time) {
              var comps = time.split(':');
              var i = 0;
              var value = Math.round(parseInt(comps[i], 10)).toString();
              var len = comps.length;
              while (value == 0) {
                value = Math.round(parseInt(comps[++i], 10)).toString();
                len--;
              }
              var unit;
              switch (len) {
                case 1:
                  unit = 's';
                  break;
                case 2:
                  unit = 'm';
                  break;
                case 3:
                  unit = 'h';
                  break;
                case 4:
                  unit = 'd';
                  break;
              }
              return value + unit;
            },
